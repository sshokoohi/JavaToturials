package com.Trainingrite.DemoApp;

public class Arrays {

	public static void main(String[] args) {
		// Define an array
		
		String valueOfNumber[] = new String[3];
		
		// Assign value to array
		valueOfNumber[0] = "100";
		valueOfNumber[1] = "200";
		valueOfNumber[2] = "300";
		
		//Print value from array
		System.out.println(valueOfNumber[0]);
		System.out.println(valueOfNumber[1]);
		System.out.println(valueOfNumber[2]);

	}

}
